"""
Tipo None

O tipo de dados None em Python, representa o tipo sem tipo, ou poderia ser conhecido também
como tipo vaziom porém, falar que é um tipo sem tipo é mais apropriado

Obs O tipo None é sempre especificado com a primeira letra maiúscula.

Certo : None
Errado : none

Quando utilizar?

- Podemos utilizar None quando queremos criar uma variável e inicializá-la com um tipo sem tipo, antes de
recebermo u valorr fibnal.
"""

